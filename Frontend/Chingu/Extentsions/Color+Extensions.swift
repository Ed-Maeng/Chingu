import SwiftUI

// This extension adds our custom color names to SwiftUI's Color type.
extension Color {
    static let PrimaryGreen = Color("PrimaryGreen")
    static let SecondaryBlue = Color("SecondaryBlue")
    static let SecondaryBlueLight = Color("SecondaryBlueLight")
    static let SecondaryBlueDark = Color("SecondaryBlueDark")
}
